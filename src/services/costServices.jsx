export const getCosts = () => {
    return fetch(`http://localhost:8000/costs`,
    {
        method: "GET",
        headers: {
            Authorization: `Token ${localStorage.getItem("auth_token")}`,
            "Content-Type": "application/json"
        }
      }).then((res) => res.json())
}

export const getCostById = (id) => {
    return fetch(`http://localhost:8000/costs/${id}`,
    {
        method: "GET",
        headers: {
            Authorization: `Token ${localStorage.getItem("auth_token")}`,
            "Content-Type": "application/json"
        }
      }).then((res) => res.json())
}